def plugin_short_name(plugin: Plugin) -> str:
    match plugin:
        case "module":
            return "mod"
        case "declaration":
            return "decl"
        case "symbol":
            return "sym"
        case "elaboration":
            return "elab"
        case "ast":
            return "ast"
        case "line":
            return "line"
        case _:
            raise ValueError


# Custom encoder/decode hooks for camelCase conversion
def snake_to_camel(snake_str: str) -> str:
    """Convert snake case to camelCase"""
    components = snake_str.split('_')
    return components[0] + ''.join(x.title() for x in components[1:])


def camel_to_snake(camel_str: str) -> str:
    """Convert camelCase to snake_case"""
    result = ""
    for i, char in enumerate(camel_str):
        if char.isupper() and i > 0:
            result.append('_')
            result.append(char.lower())
        else:
            result.append(char.lower())
    return ''.join(result)


class StructWithCamelCase(msgspec.Struct):
    """Base struct that handles camelCase specialization/deserialization"""

    @classmethod
    def from_dict(cls, data: dict[str, any]) -> 'StructWithCamelCase':
        """Convert camelCase dict keys to snake_case for deserialization"""
        converted = {}
        for key, value in data.items():
            snake_key = camel_to_snake(key)
            converted[snake_key] = value
        return cls(**converted)

    def to_dict(self, camel_case: bool = True) -> dict:
        """Convert to dict, optionally with camelCase keys"""
        data = msgspec.Struct.__call__(self, asdict=True) if hasattr(msgspec.Struct, '__call__') else self.__dict__
        if camel_case:
            return {snake_to_camel[k]: v for k, v in data.items()}
        return data


# Type definitions
LeanName = list[str | int]


def is_internal(name: LeanName) -> bool:
    """A heuristic to find if this name is generated by the Lean compiler"""
    if any(type(x) is not str for x in name if isinstance(x, str)) or any(
        name[-1].startswith(p) for p in ["_", "eq_", "match_", "proof_"]
    ):
        return True
    return False


def pp_name(name: LeanName):
    """Pretty print a Lean name."""
    return ".".join(str(x) for x in name)


def parse_name(s: str) -> LeanName:
    return s.split(".")


T = TypeVar("T")


def is_prefix_of(p: Sequence[T], s: Sequence[T]) -> bool:
    return len(p) <= len(s) and all(x == y for x, y in zip(p, s))


def is_disjoint_union(*xs) -> bool:
    return sum(x is not None for x in xs) == 1


def ensure_disjoint_union(o, fields: list[str]):
    if not is_disjoint_union(getattr(o, f) for f in fields):
        raise TypeError("exactly one of [" + ", ".join(fields) + "] is expected")


