# Serialization helpers
def to_json(obj: msgspec.Struct, camel_case: bool = True) -> str:
    """Serialize msgspec struct to JSON string"""
    encoder = msgspec.json.Encoder()
    return encoder.encode(obj).decode()


def from_json(data: str, struct_type: type) -> Any:
    """Deserialize JSON string to msgspec struct"""
    decoder = msgspec.json.Decoder(type=struct_type)
    return decoder.decode(data.encode())


def to_json_file(obj: Union[msgspec.Struct, list], filename: AnyPath) -> None:
    """Write msgspec structure to JSON file"""
    with open(filename, "wb") as fp:
        encoder = msgspec.json.Encoder()
        fp.write(encoder.encode(obj))


def from_json_file(filename: AnyPath, struct_type: type) -> Any:
    """Read msgspec struct from JSON file"""
    try:
        with open(filename, 'rb', encoding='utf-8') as fp:
            decoder = msgspec.json.Decoder(type=list[struct_type])
            return decoder.decode(fp.read())
    except msgspec.DecodeError as e:
        print(f"\n❌ JSON decode failed file: {filename}")
        print(f"❌ Error: {e}")
        raise
    except Exception as e:
        print(f"❌ Error reading file: {e}")
        raise
